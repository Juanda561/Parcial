
public class InterfazRed {
    
    private int OA;
    private int OB;
    private int OC;
    private int OD;
    

    public InterfazRed(int OA, int OB, int OC, int OD) {
        this.OA = OA;
        this.OB = OB;
        this.OC = OC;
        this.OD = OD;
    }

    public int getOA() {
        return OA;
    }

    public void setOA(int OA) {
        this.OA = OA;
    }

    public int getOB() {
        return OB;
    }

    public void setOB(int OB) {
        this.OB = OB;
    }

    public int getOC() {
        return OC;
    }

    public void setOC(int OC) {
        this.OC = OC;
    }

    public int getOD() {
        return OD;
    }

    public void setOD(int OD) {
        this.OD = OD;
    }
    
    
}
